# items():- Returns a list containing a tuple for each key value pair.
#           It basically means it encapsulates key-value pair int tuple
#           and again the tuples are encapsulated into list.
dictionary={1: "Software", 2: "Development", 3: "Competitive", 4: "Programming"}
print("The dictionary items: ", dictionary)
print("Returns a list containing a tuple for each key value pair: ", dictionary.items())