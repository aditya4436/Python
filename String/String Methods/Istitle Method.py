# istitle():- Returns True if all words in a text start with a upper case
#             letter, and rest of the word are lower case letters, otherwise
#             false.
string="PYTHON"
string1="Python Programming Language"
string2="123 Aditya"
string3="This is @ Python ! String"
print(string.istitle())
print(string1.istitle())
print(string2.istitle())
print(string3.istitle())