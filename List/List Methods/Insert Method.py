# insert():- Inserts a given element at a given index in a list.
#              Note:- The first argument must be the index where 
#              you wish to insert an element and the second argument 
#              must be the element that you want to insert.
list=[1, 3, 5, 7, 9]
print(f"The list before insert an element: {list}.")
list.insert(2, 10)
print(f"The list after inserting an element: {list}")